# 📦 Variable = Toy Box
# You have a toy box named 'name', and inside is 'Rahul'

name = "Rahul"
print("Name in the box is:", name)

# 🍎 Integer (int) - Whole numbers
apples = 5
print("Apples:", apples)

# 🥛 Float - Numbers with a dot
milk = 1.5
print("Milk in liters:", milk)

# 🐱 String - Words or text
pet = "Tom"
print("My pet's name is:", pet)

# 🚦 Boolean - Only 2 answers
light_on = True
print("Is the light on?", light_on)
