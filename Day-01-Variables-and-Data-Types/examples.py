# ======================================
# Variables Examples
# ======================================

# Example 1: Storing a name
name = "Rahul"
print(name)

# Example 2: Storing age
age = 21
print(age)

# Example 3: Multiple Variables
name = "Rahul"
age = 21
city = "Hyderabad"

print(name)
print(age)
print(city)

# Example 4: Updating Variables
score = 100
print(score)

score = 200
print(score)

# Example 5: Copying Variables
x = 10
y = x

print(x)
print(y)

# Example 6: Multiple Assignment
a, b, c = 10, 20, 30

print(a)
print(b)
print(c)

# Example 7: Real World Example

username = "rahul123"
followers = 1500
verified = False

print(username)
print(followers)
print(verified)
